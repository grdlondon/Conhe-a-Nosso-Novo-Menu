function button(){
    if(document.getElementById("imagem1").classList.contains("Show")){
      document.getElementById("imagem1").classList.remove("Show")
      document.getElementById("imagem1").classList.add("noShow")
      document.getElementById("button1").textContent  = "Esconder"
  }else{
    document.getElementById("imagem1").classList.remove("noShow")
    document.getElementById("imagem1").classList.add("Show")
    document.getElementById("button1").textContent  = "Mostrar"
  }
  }
